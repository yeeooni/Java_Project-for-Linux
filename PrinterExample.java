public class PrinterExample{

	//field 선언
		
		int num;
		boolean x;
		double dd;
		String name;


	//method 선언
	public int println(int num){
			
			return num;
	}

	public boolean println(boolean x){
			this.x = x;
			return this.x;
	}

	public double println(double dd){
		
			return dd;
	}

	public String println(String name){
			return name;		
	}
}	
